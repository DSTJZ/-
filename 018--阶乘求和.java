public class Main
{   public static void main(String[] args)
	{	long f = 1;
		long sum  = 0;
		long mod = (long)1e9;
		for(int i = 1; i <= 40; i++)//当阶乘在f(40)以后，所有阶乘数的后9位都是0；不影响结果
		{	f *= i;
			f %= mod;
			sum += f;
			sum %= mod;
		}
		System.out.print(sum);
	}
}
